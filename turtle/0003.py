import turtle

pen = turtle.Turtle()

distance = 200 #distance by which we push the pen
angle = 90 #in degrees

pen.forward(distance) #push the pen forward by the distance "distance"
pen.right(angle) #turn the arrowhead of the pen to the right by the angle "angle"
pen.forward(distance)
pen.right(angle) 
pen.forward(distance)
