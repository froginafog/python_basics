from sympy import Float

#scientific notation
print("Float('123.456E-2'):", Float('123.456E-2')) #123.456 * 10^-2
print("Float('123.456E-1'):", Float('123.456E-1')) #123.456 * 10^-1
print("Float('123.456E0'):", Float('123.456E0')) #123.456 * 10^0
print("Float('123.456E1'):", Float('123.456E1')) #123.456 * 10^1
print("Float('123.456E2'):", Float('123.456E2')) #123.456 * 10^2

