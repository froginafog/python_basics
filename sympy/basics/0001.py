from sympy import sqrt, evalf

print("sqrt(2):", sqrt(2)) 
print("sqrt(9):", sqrt(9))

#returns the exact value only
#to get the approximate value, use evalf()

print("sqrt(2).evalf():", sqrt(2).evalf()) 
print("sqrt(9).evalf():", sqrt(9).evalf())

"""
sqrt(2): sqrt(2)
sqrt(9): 3
sqrt(2).evalf(): 1.41421356237310
sqrt(9).evalf(): 3.00000000000000
"""
