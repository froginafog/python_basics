from sympy import Matrix , pprint

A = [
        [1, 2, 3],
        [4, 5, 6],
        [7, 8, 9]
    ]

B = [
        [1, 4, 7],
        [2, 5, 8],
        [3, 6, 9]
    ]

A = Matrix(A)
B = Matrix(B)

print("A + B:")
pprint(A + B)
print("----------------------")
print("A - B:")
pprint(A - B)
print("----------------------")
print("A * B:")
pprint(A * B)
print("----------------------")

