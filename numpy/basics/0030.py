import numpy as np

A = [[1, 2],
     [3, 4]]

B = [[5, 6],
     [7, 8]]

A = np.array(A)
B = np.array(B)

print("A:")
print(A)
print("------------------------")

print("B:")
print(B)
print("------------------------")

print("A.T:")
print(A.T) #transpose of A
print("------------------------")

print("B.T:")
print(B.T) #transpose of B
print("------------------------")

