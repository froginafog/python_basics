import math

print("pi/2 in degrees is:", math.degrees(math.pi/2))
print("pi in degrees is:", math.degrees(math.pi))
print("3*pi/2 in degrees is:", math.degrees(3*math.pi/2))
print("2*pi in degrees is:", math.degrees(2*math.pi))

